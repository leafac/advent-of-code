const map = input
  .trim()
  .split("\n")
  .map((line) => line.split(""));

function printMap(visitedPositions) {
  let mapString = "";
  for (let y = 0; y < map.length; y++) {
    for (let x = 0; x < map[0].length; x++)
      mapString += visitedPositions.has(JSON.stringify({ x, y }))
        ? "◉"
        : map[y][x];
    mapString += "\n";
  }
  console.log(mapString);
}

const worklist = [
  {
    position: { x: 1, y: 0 },
    direction: undefined,
    goAgainstTheSlope: false,
    visitedPositions: new Set(),
  },
];
const hikeLengths = [];
while (worklist.length > 0) {
  const state = worklist.pop();
  const positionKey = JSON.stringify(state.position);
  if (
    state.position.x < 0 ||
    state.position.x >= map[0].length ||
    state.position.y < 0 ||
    state.position.y >= map.length ||
    state.visitedPositions.has(positionKey) ||
    map[state.position.y][state.position.x] === "#" ||
    (map[state.position.y][state.position.x] !== "." &&
      ((state.goAgainstTheSlope &&
        map[state.position.y][state.position.x] !== state.direction) ||
        (!state.goAgainstTheSlope &&
          map[state.position.y][state.position.x] === state.direction)))
  )
    continue;
  const goAgainstTheSlope =
    map[state.position.y][state.position.x] === "."
      ? state.goAgainstTheSlope
      : !state.goAgainstTheSlope;
  if (state.position.y === map.length - 1) {
    console.log(state.visitedPositions.size);
    printMap(state.visitedPositions);
    hikeLengths.push(state.visitedPositions.size);
  }
  const visitedPositions = new Set([...state.visitedPositions, positionKey]);
  worklist.push(
    {
      position: { x: state.position.x, y: state.position.y - 1 },
      direction: "^",
      goAgainstTheSlope,
      visitedPositions,
    },
    {
      position: { x: state.position.x + 1, y: state.position.y },
      direction: ">",
      goAgainstTheSlope,
      visitedPositions,
    },
    {
      position: { x: state.position.x, y: state.position.y + 1 },
      direction: "v",
      goAgainstTheSlope,
      visitedPositions,
    },
    {
      position: { x: state.position.x - 1, y: state.position.y },
      direction: "<",
      goAgainstTheSlope,
      visitedPositions,
    }
  );
}

console.log(hikeLengths.sort((a, b) => a - b).at(-1));
